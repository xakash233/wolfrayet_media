  "social-ads": "11000011,11000011,00111100,00111100,11000011,11000011,00111100,11111111",
  content: "11111111,01000010,01011010,01011010,01000010,01011010,01000010,11111111",
  email: "10000001,01000010,00100100,00011000,00011000,00100100,01000010,10000001",
  "web-development": "11100111,10000001,10111001,10111001,10111001,10000001,11100111,00000000",
  branding: "00111100,01100110,11000011,11000011,01100110,00111100,00011000,00011000",
  cro: "00011000,00111100,01111110,11111111,01111110,00111100,00011000,00000000",
  orm: "00011000,00111100,01111110,11111111,01111110,00111100,00011000,00000000",
  influencer: "10000001,01000010,00100100,01000010,10000001,10111101,10000001,11111111",
  video: "11111111,10000001,10111101,10000001,10111101,10000001,11111111,00000000",
